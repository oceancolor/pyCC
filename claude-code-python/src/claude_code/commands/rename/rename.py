"""
Ported from: commands/rename/rename.ts

/rename [name] command — rename the current Claude Code session.

If no name is provided, a name is auto-generated by asking Haiku to
summarise the conversation.  The new name is saved to the transcript for
persistence and reflected immediately in the app state.
"""
from __future__ import annotations

import asyncio
from typing import Callable, Dict, Optional


# ---------------------------------------------------------------------------
# Stub helpers
# ---------------------------------------------------------------------------

def _is_teammate() -> bool:
    try:
        from claude_code.utils.teammate import is_teammate  # type: ignore[import]
        return is_teammate()
    except ImportError:
        return False


def _get_session_id() -> str:
    try:
        from claude_code.bootstrap.state import get_session_id  # type: ignore[import]
        return get_session_id()
    except ImportError:
        import uuid
        return str(uuid.uuid4())


def _get_transcript_path() -> str:
    try:
        from claude_code.utils.session_storage import get_transcript_path  # type: ignore[import]
        return get_transcript_path()
    except ImportError:
        import os
        return os.path.join(os.path.expanduser("~"), ".claude", "transcript.jsonl")


async def _save_custom_title(session_id: str, title: str, path: str) -> None:
    try:
        from claude_code.utils.session_storage import save_custom_title  # type: ignore[import]
        await save_custom_title(session_id, title, path)
    except ImportError:
        pass


async def _save_agent_name(session_id: str, name: str, path: str) -> None:
    try:
        from claude_code.utils.session_storage import save_agent_name  # type: ignore[import]
        await save_agent_name(session_id, name, path)
    except ImportError:
        pass


def _get_messages_after_compact_boundary(messages) -> list:
    try:
        from claude_code.utils.messages import get_messages_after_compact_boundary  # type: ignore[import]
        return get_messages_after_compact_boundary(messages)
    except ImportError:
        return messages if isinstance(messages, list) else []


async def _update_bridge_session_title(
    bridge_session_id: str,
    new_name: str,
    app_state: dict,
) -> None:
    """Best-effort sync of the title to claude.ai/code (non-blocking)."""
    try:
        from claude_code.bridge.bridge_config import (  # type: ignore[import]
            get_bridge_base_url_override,
            get_bridge_token_override,
        )
        from claude_code.bridge.create_session import update_bridge_session_title  # type: ignore[import]

        base_url = get_bridge_base_url_override()
        token_override = get_bridge_token_override()

        await update_bridge_session_title(
            bridge_session_id,
            new_name,
            base_url=base_url,
            get_access_token=(lambda: token_override) if token_override else None,
        )
    except (ImportError, Exception):  # noqa: BLE001
        pass  # Best-effort; non-fatal


# ---------------------------------------------------------------------------
# Command entry point
# ---------------------------------------------------------------------------

async def call(
    on_done: Callable[[str, dict], None],
    context: object,
    args: str,
) -> None:
    """
    Handle the /rename [name] command.

    Parameters
    ----------
    on_done:
        Callback ``(message: str, options: dict) -> None``.
    context:
        ToolUseContext + LocalJSXCommandContext duck-typed object.
    args:
        Optional new session name; if empty, an AI-generated name is used.
    """
    from .generate_session_name import generate_session_name  # local import

    # Teammates' names are set by the swarm leader
    if _is_teammate():
        on_done(
            "Cannot rename: This session is a swarm teammate. "
            "Teammate names are set by the team leader.",
            {"display": "system"},
        )
        return

    new_name: Optional[str]

    if not args or not args.strip():
        # Auto-generate from conversation context
        messages = getattr(context, "messages", [])
        after_compact = _get_messages_after_compact_boundary(messages)
        abort_controller = getattr(context, "abort_controller", None)
        signal = getattr(abort_controller, "signal", None)

        generated = await generate_session_name(after_compact, signal=signal)
        if not generated:
            on_done(
                "Could not generate a name: no conversation context yet. "
                "Usage: /rename <name>",
                {"display": "system"},
            )
            return
        new_name = generated
    else:
        new_name = args.strip()

    session_id = _get_session_id()
    transcript_path = _get_transcript_path()

    await _save_custom_title(session_id, new_name, transcript_path)

    # Sync to bridge session on claude.ai/code (best-effort)
    get_app_state = getattr(context, "get_app_state", None)
    if get_app_state is not None:
        try:
            app_state: dict = get_app_state()
            bridge_session_id = app_state.get("replBridgeSessionId")
            if bridge_session_id:
                asyncio.ensure_future(
                    _update_bridge_session_title(bridge_session_id, new_name, app_state)
                )
        except Exception:  # noqa: BLE001
            pass

    await _save_agent_name(session_id, new_name, transcript_path)

    # Reflect immediately in app state
    set_app_state = getattr(context, "set_app_state", None)
    if set_app_state is not None:
        def _update(prev: dict) -> dict:
            updated = dict(prev)
            sac = dict(prev.get("standalone_agent_context") or {})
            sac["name"] = new_name
            updated["standalone_agent_context"] = sac
            return updated
        set_app_state(_update)

    on_done(f"Session renamed to: {new_name}", {"display": "system"})
